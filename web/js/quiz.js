function restartQuiz(){
    quizFinished = false;
    score = 0;
    userAnswers[currentBab] = {};
    document.getElementById("quizScore").innerText = "0";
    loadQuiz(currentBab);
}

window.restartQuiz = restartQuiz;
window.submitQuiz = submitQuiz;
window.openQuiz = openQuiz;
