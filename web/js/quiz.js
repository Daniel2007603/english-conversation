let currentBab = null;
let score = 0;

function openQuiz(bab){
    currentBab = bab;
    score = 0; // 🔥 RESET SCORE SETIAP QUIZ
    document.getElementById("quizScore").innerText = score;

    showSection("quiz");
    loadQuiz(bab);
}

function loadQuiz(bab){
    const container = document.getElementById("quizContainer");
    container.innerHTML = "";

    const data = quizBank[bab];

    if(!data || data.length === 0){
        container.innerHTML = "<h3>No quiz found</h3>";
        return;
    }

    data.forEach((q, index)=>{
        const div = document.createElement("div");
        div.classList.add("card");

        div.innerHTML = `
            <p><b>${q.question}</b></p>

            <button onclick="selectAnswer(${index},0,this)">
                ${q.options[0]}
            </button>

            <button onclick="selectAnswer(${index},1,this)">
                ${q.options[1]}
            </button>

            <div id="result${index}"></div>
        `;

        container.appendChild(div);
    });
}

function selectAnswer(questionIndex, selected, button){
    const result = document.getElementById(`result${questionIndex}`);
    const data = quizBank[currentBab];

    if(!data) return;

    const parent = button.parentElement;

    if(parent.classList.contains("answered")){
        return;
    }

    parent.classList.add("answered");

    if(selected === data[questionIndex].answer){
        result.innerHTML = "✅ Correct";
        score++;
        addXP(5);
    } else {
        result.innerHTML = "❌ Wrong";
    }

    document.getElementById("quizScore").innerText = score;
}

function finishQuiz(){
    if(currentBab === null){
        alert("Pilih quiz dulu!");
        return;
    }

    alert(`Quiz selesai!\nScore kamu: ${score}`);

    // optional: reset UI
    document.getElementById("quizContainer").innerHTML = "";
}

function submitQuiz(){

    if(quizFinished) return;

    const data = quizBank[currentBab];

    let finalScore = 0;

    data.forEach((q, index)=>{
        if(userAnswers[currentBab]?.[index] === q.answer){
            finalScore++;
        }
    });

    quizFinished = true;

    document.getElementById("quizScore").innerText = finalScore;

    const container = document.getElementById("quizContainer");

    container.innerHTML = `
        <div class="card">
            <h2>🎉 Quiz Finished!</h2>
            <h1>${finalScore} / ${data.length}</h1>

            <button onclick="restartQuiz()">
                🔁 Try Again
            </button>

            <button onclick="showSection('materials')">
                📚 Back to Materials
            </button>
        </div>
    `;
}

